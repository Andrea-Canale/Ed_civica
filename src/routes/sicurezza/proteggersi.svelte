    <h1>Esegui copie di backup dei dati importanti</h1>
    <p class="text">
      Il ransomware è una delle minacce più complesse da gestire, in quanto le
      soluzioni a disposizione dell’utente sono davvero ridotte al minimo. Nel
      caso in cui non sia possibile prevenire l’attacco, infatti, l’unico
      rimedio utile potrebbe essere quello del ripristino del dispositivo, che
      comporta però la perdita di tutti i dati. Per questi motivi diventa
      indispensabile provvedere regolarmente all’esecuzione e alla copia di
      backup dei dati almeno di quelli più importanti.
