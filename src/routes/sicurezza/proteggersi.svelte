    <h1>Scegliere password complesse</h1>
    <div align="center" class="container">
      <p class="text">
        Le password rappresentano delle vere e proprie chiavi di accesso segrete
        alle informazioni più importanti in possesso. Il mondo del web conserva
        oggi quasi tutti i propri dati: conti correnti, informazioni sanitarie,
        account business e molto altro. Per questo motivo scegliere password
        molto complesse, lunghe e difficili da indovinare risulta essere vitale
        in qualsiasi sito Web.
      </p>
