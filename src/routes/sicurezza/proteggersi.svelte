      pericolosa. Le piattaforme di messaggistica dispongono oggi di elevati
      sistemi di sicurezza dei dati personali, ma tanti sono gli scenari in cui
      gli elementi condivisi potrebbero finire nelle mani di utenti poco
      raccomandabili. Cercare quindi sempre di evitare di inviare numeri di
      carte di pagamento, password o dati di accesso.
    </p>
